package versaogama.model.system.movprodutos;

public class ModelValorUnitarioDoProduto {
	
	private String ano;
	private String mes;
	private String codItem;
	private Double totQtde;
	private Double vlUnit;
	private Double vlTotItem;
	
	
	public String getAno() {
		return ano;
	}
	public void setAno(String ano) {
		this.ano = ano;
	}
	public String getMes() {
		return mes;
	}
	public void setMes(String mes) {
		this.mes = mes;
	}
	public String getCodItem() {
		return codItem;
	}
	public void setCodItem(String codItem) {
		this.codItem = codItem;
	}
	public Double getTotQtde() {
		return totQtde;
	}
	public void setTotQtde(Double totQtde) {
		this.totQtde = totQtde;
	}
	public Double getVlUnit() {
		return vlUnit;
	}
	public void setVlUnit(Double vlUnit) {
		this.vlUnit = vlUnit;
	}
	public Double getVlTotItem() {
		return vlTotItem;
	}
	public void setVlTotItem(Double vlTotItem) {
		this.vlTotItem = vlTotItem;
	}
	
	
	

}
