package versaogama.model.system.estabelecimento;

public class Municipio {

	private Long id;
	private Long ufId;
	private String nome;
	
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getUfId() {
		return ufId;
	}
	public void setUfId(Long ufId) {
		this.ufId = ufId;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
}
